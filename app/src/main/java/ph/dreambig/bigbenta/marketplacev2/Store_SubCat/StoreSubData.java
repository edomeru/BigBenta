package ph.dreambig.bigbenta.marketplacev2.Store_SubCat;


public class StoreSubData {

    String ImgUrl;
    String Name;

    public StoreSubData(String imgUrl, String name) {
        ImgUrl = imgUrl;
        Name = name;
    }

    public String getImgUrl() {
        return ImgUrl;
    }

    public String getName() {
        return Name;
    }
}
