public class AdsSubData {

    String ImgUrl;
    String Name;

    public AdsSubData(String imgUrl, String name) {
        ImgUrl = imgUrl;
        Name = name;
    }

    public String getImgUrl() {
        return ImgUrl;
    }

    public String getName() {
        return Name;
    }
}
